const server = http.createServer((req, res) => {
    // –û–±—Ä–∞–±–æ—Ç–∫–∞ CORS
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'GET, POST, OPTIONS');
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
    res.setHeader('Cache-Control', 'no-cache, no-store, must-revalidate');

    // –û–±—Ä–∞–±–æ—Ç–∫–∞ OPTIONS –∑–∞–ø—Ä–æ—Å–æ–≤ –¥–ª—è CORS
    if (req.method === 'OPTIONS') {
        res.writeHead(200);
        res.end();
        return;
    }

    // –ü–∞—Ä—Å–∏–º URL
    let pathname = url.parse(req.url).pathname;
    
    // –ï—Å–ª–∏ –∑–∞–ø—Ä–∞—à–∏–≤–∞–µ—Ç—Å—è –∫–æ—Ä–µ–Ω—å, –æ—Ç–¥–∞–µ–º index.html
    if (pathname === '/') {
        pathname = '/index.html';
    }

    // –ü–æ–ª–Ω—ã–π –ø—É—Ç—å –∫ —Ñ–∞–π–ª—É
    const filePath = path.join(DIRECTORY, pathname);

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ —Ñ–∞–π–ª–∞
    fs.access(filePath, fs.constants.R_OK, (err) => {
        if (err) {
            // –§–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω
            res.writeHead(404, { 'Content-Type': 'text/html' });
            res.end('<h1>404 - –§–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω</h1>');
            console.log(`[404] ${pathname}`);
            return;
        }

        // –û–ø—Ä–µ–¥–µ–ª—è–µ–º MIME —Ç–∏–ø
        const ext = path.extname(filePath);
        const mimeType = mimeTypes[ext] || 'application/octet-stream';

        // –ß–∏—Ç–∞–µ–º –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º —Ñ–∞–π–ª
        fs.readFile(filePath, (err, data) => {
            if (err) {
                res.writeHead(500);
                res.end('–û—à–∏–±–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞');
                console.error(`[500] –û—à–∏–±–∫–∞ —á—Ç–µ–Ω–∏—è —Ñ–∞–π–ª–∞: ${err}`);
                return;
            }

            res.writeHead(200, { 'Content-Type': mimeType });
            res.end(data);
            console.log(`[200] ${pathname}`);
        });
    });
});

