// If already logged in, redirect to main app
let redirecting = false;
auth.onAuthStateChanged(async (user) => {
    if (user && !redirecting) {
        console.log('üîê User already logged in:', user.email || user.phoneNumber);
        
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º –µ—Å—Ç—å –ª–∏ –ø—Ä–æ—Ñ–∏–ª—å –≤ Firestore
        try {
            const userDoc = await db.collection('users').doc(user.uid).get();
            if (userDoc.exists) {
                console.log('‚úÖ User profile exists, redirecting...');
                redirecting = true;
                window.location.href = '/';
            } else {
                console.log('‚ö†Ô∏è User profile not found, staying on login page');
                // –°–æ–∑–¥–∞–µ–º –ø—Ä–æ—Ñ–∏–ª—å
                await createUserProfile(user);
                redirecting = true;
                window.location.href = '/';
            }
        } catch (error) {
            console.error('Error checking user profile:', error);
        }
    }
});

